#include "3-calc.h"

/**
 * add - add function
 * @a: int type
 * @b: int type
 * Return: sum of two int
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substract function
 * @a: int type
 * @b: int type
 * Return: subtract two ints
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply function
 * @a: int type
 * @b: int type
 * Return: multiply two ints
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two ints
 * @a: int type
 * @b: int type
 * Return: divide two ints
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulos of two ints
 * @a: int type
 * @b: int type
 * Return: modulus of two ints
 */

int mod(int a, int b)
{
	return (a % b);
}
